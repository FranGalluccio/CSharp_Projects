using System;

namespace I_cicli
{
    class Program
    {
        static void Main(string[] args)
        {
            // Ciclo while

            while (true)
            {
                Console.Write("Hello World! ");
            }


            //Ciclo do while
            
            do
            {
                Console.Write("Hello world! ");
            }
            while (true);

        }
    }
}
